create table
  public.bank (
    id bigint generated by default as identity not null,
    created_at timestamp with time zone null default now(),
    spins bigint null default '400'::bigint,
    gold bigint null default '0'::bigint,
    affection bigint null default '400'::bigint,
    shields smallint null default '0'::smallint,
    followers bigint null default '0'::bigint,
    sessions bigint null default '0'::bigint,
    conquests bigint null default '0'::bigint,
    time_played bigint null default '0'::bigint,
    times_spun bigint null default '0'::bigint,
    rescues bigint null default '0'::bigint,
    conversions bigint null default '0'::bigint,
    updated_at timestamp with time zone null default (now() at time zone 'utc'::text),
    user_id uuid null,
    level integer null default 1,
    attack_power smallint null default '0'::smallint,
    special_power smallint null default '0'::smallint,
    convert_power integer null default 0,
    constraint bank_duplicate_pkey primary key (id),
    constraint bank_user_id_key unique (user_id),
    constraint bank_user_id_fkey foreign key (user_id) references auth.users (id) on delete set null
  ) tablespace pg_default;